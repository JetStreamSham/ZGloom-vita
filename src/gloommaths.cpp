	void CalcNormVec(int16_t x, int16_t y, int16_t& xr, int16_t& yr)
	{
		/*
		calcnormvec; d0, d1 = vector...normalize!
		;
		; OK, find vector length!
		;
		move	d0, d2
		muls	d2, d2
		move	d1, d3
		muls	d3, d3
		add.l	d3, d2
		;
		*/
		int32_t x2 = x;
		x2 *= x;
		int32_t y2 = y;
		y2 *= y;

		x2 = x2 + y2;
		/*
		; OK, sqr of d2.l!
		;
		move.l	#$10000, d3
		;
		.fitit	cmp.l	#16384, d2; fits ?
		bcs.s.ok
		asr.l	#1, d2
		mulu.l	#92681, d4:d3; mult by sqr(2)
		move	d4, d3
		swap	d3
		bra.s.fitit
		;
		*/
		int64_t multiplier = 0x10000;

		while (x2 >= 16384)
		{
			x2 >>= 1;
			multiplier *= 92681;
			multiplier >>= 16; // the move/swap combo shifts a quadword down by 16, effectively, as it's a move.w.
		}
		/*
		.ok; OK to look up, but multiply the result.w by d3.q
		;
		move.l	sqr(pc), a0
		and	#$fffe, d2
		movem	0(a0, d2), d2; sqr.l!
		mulu.l	d3, d2
		swap	d2; length.w
		*/
		x2 = sqrtable[x2 >> 1];
		x2 *= (int32_t)multiplier;
		x2 >>= 16;
		/*
		;
		; length should be >= both abs(xvec) AND abs(zvec)
		;
		move	d0, d3
		bpl.s.xp
		neg	d3
	.xp	move	d1, d4
		bpl.s.zp
		neg	d4
	.zp;
		cmp	d4, d3
		bcc.s.bg
		exg	d3, d4
		.bg;
		cmp	d3, d2
		bcc.s.lo
		move	d3, d2
	.lo;
	*/
		if (x2 < abs(x)) x2 = abs(x);
		if (x2 < abs(y)) x2 = abs(y);
	/*

		ext.l	d2
		;
		swap	d0
		clr	d0
		divs.l	d2, d0
		muls.l	#32766, d0
		swap	d0
		;
		swap	d1
		clr	d1
		divs.l	d2, d1
		muls.l	#32766, d1
		swap	d1
		rts
		*/
		int32_t xq = x;
		xq <<= 16;
		xq /= x2;
		xq *= 32766;
		xq >>= 16;

		int32_t yq = y;
		yq <<= 16;
		yq /= x2;
		yq *= 32766;
		yq >>= 16;

		xr = xq;
		yr = yq;
	}
