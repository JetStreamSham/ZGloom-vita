	uint8_t CalcAngle(int32_t x1, int32_t z1, int32_t x2, int32_t z2)
	{
		const unsigned int octtable[8] = { 0, 0x4000FFFF, 0x0000FFFF, 0xc0000000, 0x8000FFFF, 0x40000000, 0x80000000, 0xc000FFFF };

		///OCTTABLE INDEXED BY LONGS, IN BYTES IN THE ASSEMBLY BELOW, HENCE DIV BY 4

		/*
		calcangle_; d0.w = x d1.w = y(dest - src)!
			;
			moveq	#0, d2
			tst	d1
			bpl.s.hpos
			moveq	#16, d2
			neg	d1
			.hpos	tst	d0
			bpl.s.wpos
			eor	#8, d2
			neg	d0
		*/

		uint32_t oct = 0;
		x1 -= x2;
		z1 -= z2;

		if (z1 < 0)
		{
			oct = 4;
			z1 = -z1;
		}
		if (x1 < 0)
		{
			oct ^= 2;
			x1 = -x1;
		}

		/*
		.wpos	cmp	d1, d0
				bmi.s.notsteep
				bne.s.neq
				move	#$2000, d1
				bra.s.flow
		.neq	eor	#4, d2
				exg	d1, d0
		.notsteep	tst	d1
				bne.s.noflow
				moveq	#0, d1
				bra.s.flow
		.noflow	ext.l	d0
				swap	d0
				divu	d1, d0
				lsr	#6, d0
				and	#1022, d0
				move.arc(pc, d0), d1
		.flow	move.l.oct(pc, d2), d0
				eor	d0, d1
				swap	d0
				add	d1, d0
				lsr	#8, d0
				rts
				;
		*/

		if ((x1 - z1) < 0)
		{
			goto notsteep;
		}
		if (z1 == x1)
		{
			z1 = 0x2000;
			goto flow;
		}
		oct ^= 1;
		std::swap(x1, z1);
	notsteep:
		if (z1 != 0) goto noflow;
		z1 = 0;// isn't this superfluous? "if z1 = 0, set z1 to zero?"
		goto flow;
	noflow:
		x1 = (x1 << 16) | ((x1 >> 16)&0xFFFF);
		x1 = x1 / z1;
		x1 >>= 6;
		x1 &= 1022;
		z1 = arctable[x1 / 2];
	flow:
		x1 = octtable[oct];
		z1 ^= x1;
		x1 >>= 16;
		x1 += z1;
		x1 >>= 8;
		return x1 & 0xFF;
	}


